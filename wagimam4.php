          <h5><b>Launching of Wagima Foundation</b></h5>

          <p>In the previous meeting held on 5th august Faith suggested that we create a project in a way of giving back to our community in from of a project and her idea was welcomed by everone and we decide to name it wagima foundation.</p>
          <p><b>Mzee Silver</b> Suggested that we launch our foundation project in september next year as children are in holidays.<br> He also said that we make it colorful so that the idea can sell even across other families in the bamugoya clan.</p>
